package com.betterjr.modules.flie.exception;

public class ImportExcelException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -199101013554326931L;

	public ImportExcelException() {
		super();
	}

	public ImportExcelException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ImportExcelException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImportExcelException(String message) {
		super(message);
	}

	public ImportExcelException(Throwable cause) {
		super(cause);
	}

	
	
}
